							<hr />
							<div style="height: 375px; overflow:auto;" class="vertical-pane">
							<p>La realt&agrave; ci appare sempre da una prospettiva che nasce dal nostro interno, da quello che i nostri sensi ci consentono di percepire.<br />
								Ma cosa si prova ad essere spiati da noi stessi attraverso una visione in cui diventiamo oggetto tra gli oggetti? Cosa accadrebbe se, all’improvviso, l’immagine di noi divenisse l’unico modo che rimane per osservare il mondo che ci circonda con noi stessi dentro di lui?
								E in pi&ugrave;, se all’improvviso l’universo che noi percepiamo ci apparisse improvvisamente del tutto mutato, irreale e mutante? <br />
								In <i>TerzOcchio project</i>, lo sguardo in soggettiva con il quale siamo abituati a scrutare il mondo si ribalta e si trasforma in una oggettiva sulla nostra soggettivit&agrave;.
								Un “ingarbugliamento lessicale” che bene rende il senso dello straniamento di chi vive questa “avventura”.<br />
								Un luogo-esperienza in cui il  visitatore &egrave; guidato da un senso a lui estraneo, la vista di un terzo occhio che diventa l’unica guida in un microcosmo in cui niente esiste realmente se non, appunto, il visitatore stesso.<br />
								Una sorta di “Irreality show”, ambientato in un mondo in cui possono materializzarsi le nostre paure, i nostri desideri in un sogno-non sogno vissuto in stato di veglia.<br />
								Moonlanding offre agli spettatori la possibilit&agrave; di immergersi in un ambiente interattivo dove lo scenario lunare &egrave; ricostruito con la tecnica del chromakey.<br />
								Mentre dall’esterno l’ambiente appare solo come una scatola a sfondo verde, chi &egrave; all’interno - indossati gli appositi occhiali – si muove attraverso il paesaggio lunare, ascoltando l’audio originale delle comunicazioni radio NASA.<br />
								Con un particolare: ci&ograve; che si vede negli occhiali non &egrave; solo lo spazio lunare, ma noi stessi immersi in esso.Bisogna perci&ograve; abituarsi ad agire in terza persona, comandando i propri movimenti dall’esterno. </p>
							
