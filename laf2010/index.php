					<div style="height: 380px; overflow:auto;" class="vertical-pane">
					<hr class="small" />
					<p>LookAtFestival, mostra di videoinstallazioni e vetrina per eventi di musica elettronica nasce a Lucca nel  2007 all’interno di un  contesto di giovani creativi interessati alle nuove forme dell’arte contemporanea.<br />
						Dalla vocazione interdisciplinare, il LookAtFestival presenta una rassegna di opere esemplari nel campo della comunicazione artistica audiovisiva, organizzando nel periodo della mostra importanti performance musicali di livello internazionale legate alle nuove espressioni della musica elettronica e sperimentale, proponendosi in area toscana e 
						nazionale come punto di riferimento per il pubblico interessato alle arti visive e alla musica. LookAtFestival  &egrave; all’avanguardia perch&eacute; al suo interno convivono tutte le pi&ugrave; attuali forme artistiche audiovisive: <br />
						visual art, video live performance, videoinstallazioni e ambienti interattivi.</p>
					<hr class="small" />
					<p>Il <strong>2007</strong> &egrave; l’anno d’esordio. La location della mostra e degli eventi musicali &egrave; la suggestiva cornice della <strong>Ex-Manifattura Tabacchi</strong>, situata nel centro storico di Lucca. 
						La manifestazione nasce da una collaborazione di artisti locali, sia per quanto riguarda concezione e allestimento delle opere che per gli eventi musicali. 
						La <strong>buona</strong> risposta di critica e pubblico &egrave; immediata e inaspettata. 
						Nel 2008 il progetto continua a crescere, si riconferma la collaborazione con la Provincia e il Comune di Lucca, arriva l’appoggio della Regione Toscana e del Festival della Creativit&agrave; e vari sponsor sostengono l’evento.</p>
